package loop.ex;

public class Ex1 {
    public static void main(String[] args) {
        int count = 1;
        while (count <= 10) {
            System.out.println(count);
            count++;
        }

        System.out.println();

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
